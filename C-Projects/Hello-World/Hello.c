// Include the standard input/output library
// This gives us access to functions like printf
#include <stdio.h>

// The main function is the entry point of every C program
// It returns an integer (int) which indicates if the program ran successfully
int main()
{
    // printf is a function that prints text to the console
    // \n creates a new line after the text
    printf("Hello, World!\n");

    // return 0 indicates the program ended successfully
    // any other number usually indicates an error
    return 0;
}